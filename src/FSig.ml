type vrn =
    [ Sum 
    | TyVrnEq
    | TyVrnSup
    | TyVrnInf
    | TyVrnInfSup
    | TyAbstr ];
type trail_info = (vrn*int);

(* [collumn] meta data for the record *)
type col = {
    col_label:string;
    col_mutable:bool;
    col_ctyp:ctyp
  };

(* a series of [ast] nodes generated by the type *)
type ty_info = {
    name_exp: exp;
    (* [int] -> [meta_int] *)

    info_exp: exp;
    (* int -> [meta_int fmt x0] *)

    exp0: exp;
    (* ai *)
    pat0: pat;
    (* ai *)
    id_exp: exp ;
    (* (ai,bi) *)

    id_pat: pat;
    (* (ai,bi) *)

    id_exps: exp list ;
    (* [ai;bi;ci] *)

    id_pats:  pat list;
    (* [ai;bi;ci]*)

    ty: ctyp;
    (* int *) 
  }
;

type vbranch =
   [= `variant of (string* ctyp list )
   | `abbrev of ident ];
type branch =
   [= `branch of (string * ctyp list) ];
(* Feed to user to compose an expession node *)
type record_col = {
    re_label: string ;
    re_mutable: bool ;
    re_info: ty_info;
  };
type record_info =  record_col list;

(* types below are used to tell fan how to produce
   function of type [ident -> ident]
 *)
type basic_id_transform =
    [= `Pre of string
    | `Post of string
    | `Fun of string id ];

type rhs_basic_id_transform =
    [= basic_id_transform
    | `Exp of string -> exp ];

type full_id_transform =
    [=  basic_id_transform
    | `Idents of (* list ident  -> ident *) vid list  -> vid 
    (* decompose to a list of ident and compose as an ident *)          
    | `Id of (* ident -> ident *) vid -> vid
    (* just pass the ident to user do ident transform *)
    | `Last of string -> (* ident *) vid
    (* pass the string, and << .$old$. .$return$. >>  *)      
    | `Obj of  string id ];

